    final void habilitarCampos(boolean valor) {
        txt_rua.setEnabled(valor);
        txt_bai.setEnabled(valor);
        txt_tel.setEnabled(valor);
        txt_data.setEnabled(valor);
        txt_nome.setEnabled(valor);

    }

    final void valores_cliente() {

        entid_cliente.setRua(txt_rua.getText());
        entid_cliente.setTelefone(txt_tel.getText());
        entid_cliente.setData(txt_data.getText());
        entid_cliente.setBairro(txt_bai.getText());
        entid_cliente.setNome(txt_nome.getText());
    }

    final void LimpaCampos() {
        txt_cod.setText("");
        txt_rua.setText("");
        txt_bai.setText("");
        txt_data.setText("");
        txt_nome.setText("");
        txt_tel.setText("");

    }
