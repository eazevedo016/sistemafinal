    private void table_cliMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_table_cliMousePressed
        habilitarCampos(true);
        entid_cliente = con_cliente.controlePreencher(Integer.parseInt(Modelo.getValueAt(table_cli.getSelectedRow(), 0).toString()));

        txt_cod.setText(entid_cliente.getCodigo() + "");
        txt_data.setText(entid_cliente.getData());
        txt_tel.setText(entid_cliente.getTelefone());
        txt_rua.setText(entid_cliente.getRua());
        txt_bai.setText(entid_cliente.getBairro());
        txt_nome.setText(entid_cliente.getNome());
        txt_cod.setText(entid_cliente.getCodigo() + "");


    }//GEN-LAST:event_table_cliMousePressed


