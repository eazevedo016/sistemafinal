    private void table_cliMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_table_cliMousePressed
        habilitarCampos(true);
        entid_cardapio = con_cardapio.controlePreencher(Integer.parseInt(Modelo.getValueAt(table_cli.getSelectedRow(),0).toString()));
        
        txt_codigo.setText(entid_cardapio.getCodigo() + "");
        txt_desc.setText(entid_cardapio.getCodigo() + "");
        cb_acompanhamento.setSelectedItem(entid_cardapio.getAcompanhamento());
        cb_tipo.setSelectedItem(entid_cardapio.getTipo());
        txt_valor.setText(entid_cardapio.getValor() + "");
        cb_tamanho.setSelectedItem(entid_cardapio.getTamanho());
    }//GEN-LAST:event_table_cliMousePressed

