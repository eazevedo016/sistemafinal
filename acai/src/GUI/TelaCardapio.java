final void habilitarCampos(boolean valor){
   txt_desc.setEnabled(valor);
   txt_valor.setEnabled(valor);
   cb_tipo.setEnabled(valor);
   cb_acompanhamento.setEnabled(valor);
   cb_tamanho.setEnabled(valor);
   
   
   
   
}

final void valores_funcionario(){
    
    
    
    entid_cardapio.setDescricao(txt_desc.getText());
    entid_cardapio.setValor(Double.parseDouble(txt_valor.getText()));
    entid_cardapio.setTipo(cb_tipo.getSelectedItem().toString());
    entid_cardapio.setAcompanhamento(cb_acompanhamento.getSelectedItem().toString());
    entid_cardapio.setTamanho(cb_tamanho.getSelectedItem().toString());
}

final void LimpaCampos(){
    txt_codigo.setText("");
    txt_desc.setText("");
    txt_valor.setText("");
    cb_tipo.setSelectedIndex(0);
    cb_acompanhamento.setSelectedIndex(0);
    cb_tamanho.setSelectedIndex(0);
    
    
  
    
    
}
